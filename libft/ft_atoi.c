#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	res;
	int		sign;
	size_t	i;

	res = 0;
	sign = 1;
	i = 0;
	if (!*str)
		return (0);
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -sign;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{	
		res = (res * 10) + (*str - '0');
		str++;
	}
	return ((int)sign * res);
}
